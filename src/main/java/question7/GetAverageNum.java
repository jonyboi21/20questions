package question7;

public class GetAverageNum {


    public static int getAverage(int[] ints) {
        int total = 0;
        for(int i=0; i< ints.length; i++){
            total = total + ints[i];
            // 0 = 0 +
        }


        return 0;
    }

    public static void main(String[] args) {


        int[] num1 = {34,23,12,56,7,12};
        System.out.println(getAverage(num1));

    }




}